def seed_surnames():
    print("성씨 한자 시딩 중...")
    rows = [{"hangul": h, "hanja": c, "strokes": s, "element": e, "is_common": True}
            for h, c, s, e in SURNAMES]
    # upsert (중복 무시)
    for i in range(0, len(rows), 20):
        batch = rows[i:i+20]
        try:
            sb.table("hanja_surnames").upsert(batch, on_conflict="hangul,hanja").execute()
            print(f"  성씨 {i+len(batch)}/{len(rows)} 완료")
        except Exception as ex:
            print(f"  성씨 오류: {ex}")
    print(f"성씨 완료: {len(rows)}건")

